// ─── Wear History Store ───────────────────────────────────────────────────────
// Persists daily outfit / wear records to localStorage.
//
// Schema:  { [dateStr: "YYYY-MM-DD"]: WearRecord }
// WearRecord: {
//   itemIds:   string[]      — closet item IDs worn that day (item-1, item-2 …)
//   note:      string        — optional free-text memo
//   photoUrl:  string|null   — base64 data URI from camera/upload (stored locally)
//   updatedAt: string        — ISO timestamp of last save
// }

const WEAR_HISTORY_KEY = "trunkroom_wear_history_v1";
const SEED_KEY         = "trunkroom_wear_history_seeded_v2"; // bump version to re-seed

// ─── Helpers ──────────────────────────────────────────────────────────────────

function loadStore() {
  try {
    const raw = localStorage.getItem(WEAR_HISTORY_KEY);
    return raw ? JSON.parse(raw) : {};
  } catch {
    return {};
  }
}

function saveStore(data) {
  try {
    localStorage.setItem(WEAR_HISTORY_KEY, JSON.stringify(data));
  } catch {
    // Storage quota exceeded — silently skip
  }
}

/** Format a Date as local YYYY-MM-DD (avoids UTC off-by-one). */
export function localDateStr(d) {
  const y  = d.getFullYear();
  const m  = String(d.getMonth() + 1).padStart(2, "0");
  const dd = String(d.getDate()).padStart(2, "0");
  return `${y}-${m}-${dd}`;
}

/** Today as local YYYY-MM-DD. */
export function todayStr() {
  return localDateStr(new Date());
}

