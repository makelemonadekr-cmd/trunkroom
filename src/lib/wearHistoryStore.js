function maybeSeedDemoData() {
  if (localStorage.getItem(SEED_KEY)) return;

  // Remove stale v1 seed so we start fresh
  localStorage.removeItem(WEAR_HISTORY_KEY);
  localStorage.removeItem("trunkroom_wear_history_seeded_v1");

  const store = {};
  const base  = new Date();
  base.setHours(12, 0, 0, 0);

  SEED_PATTERN.forEach(({ offset, itemIds, note }) => {
    const d = new Date(base);
    d.setDate(d.getDate() - offset);
    store[localDateStr(d)] = {
      itemIds,
      note,
      photoUrl:  null,
      updatedAt: d.toISOString(),
    };
  });

  saveStore(store);
  localStorage.setItem(SEED_KEY, "1");
}

// Run once at module load
maybeSeedDemoData();

// ─── CRUD ─────────────────────────────────────────────────────────────────────

/** Returns all records as { [dateStr]: WearRecord }. */
export function getAllWearHistory() {
  return loadStore();
}

/** Returns the record for a specific date (YYYY-MM-DD) or null. */
export function getWearRecord(dateStr) {
  return loadStore()[dateStr] ?? null;
}

/** Save / update a wear record for a specific date. */
export function saveWearRecord(dateStr, { itemIds = [], note = "", photoUrl = null } = {}) {
  const store = loadStore();
  store[dateStr] = { itemIds, note, photoUrl, updatedAt: new Date().toISOString() };
  saveStore(store);
}

/** Delete a wear record for a specific date. */
export function deleteWearRecord(dateStr) {
  const store = loadStore();
  delete store[dateStr];
  saveStore(store);
}

/** Returns dates that have records, sorted newest-first. */
export function getRecordedDates() {
  return Object.keys(loadStore()).sort((a, b) => b.localeCompare(a));
}

// ─── Stats ────────────────────────────────────────────────────────────────────

/** Returns { totalDays, totalItems, streak } for the habit UI. */
export function getWearStats() {
  const store = loadStore();
  const dates = Object.keys(store).sort();

  const totalDays  = dates.length;
  const totalItems = dates.reduce((sum, d) => sum + (store[d]?.itemIds?.length ?? 0), 0);

  // Streak: consecutive days going backwards from today.
  // If today is not yet recorded, start counting from yesterday.
  const base = new Date();
  base.setHours(12, 0, 0, 0);

  let streak = 0;
  for (let offset = 0; offset < 365; offset++) {
    const d   = new Date(base);
    d.setDate(d.getDate() - offset);
    const key = localDateStr(d);

    if (store[key]) {
      streak++;
    } else if (offset === 0) {
      // Today not recorded yet — continue looking backwards
    } else {
      break; // gap found
    }
  }

  return { totalDays, totalItems, streak };
}

// ─── Analytics ────────────────────────────────────────────────────────────────

/**
 * Returns a Map<itemId, wearCount> built from all recorded outfit data.
 * Useful for "자주 입은 옷" and "오랫동안 안 입은 옷" modules.
 */
export function getItemWearFrequency() {
  const store = loadStore();
  const freq  = new Map();
  Object.values(store).forEach(({ itemIds = [] }) => {
    itemIds.forEach((id) => freq.set(id, (freq.get(id) ?? 0) + 1));
  });
  return freq;
}

/**
 * Returns a Map<itemId, lastWornDateStr> — the most recent date each item
 * appeared in a wear record.
 */
export function getItemLastWornDates() {
  const store = loadStore();
  const last  = new Map();
  // Iterate dates oldest→newest so we always keep the latest date
  Object.keys(store)
    .sort()
    .forEach((dateStr) => {
      (store[dateStr].itemIds ?? []).forEach((id) => last.set(id, dateStr));
    });
  return last;
}

/**
 * Returns the last N recorded days (newest first), each as
 * { dateStr, itemIds, note, photoUrl }.
 */
export function getRecentRecords(n = 7) {
  const store = loadStore();
  return Object.keys(store)
    .sort((a, b) => b.localeCompare(a))
    .slice(0, n)
    .map((dateStr) => ({ dateStr, ...store[dateStr] }));
}
